package org.springframework.samples.petclinic.model;

/**
 * Simple representation of an individual being tracked for points.
 *
 * @author SWE Copilot
 */
public class Individual {

	private String name;

	private String color;

	public Individual() {
	}

	public Individual(String name, String color) {
		this.name = name;
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

}